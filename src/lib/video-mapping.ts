/**
 * 获取课程的视频路径
 * @param courseId 课程 ID
 * @returns 视频路径（相对于 VIDEO_BASE_PATH），如果未找到返回 undefined
 */
export function getVideoPath(courseId: string): string | undefined {
  return courseVideoMapping[courseId];
}

/**
 * 获取完整的视频 URL
 * @param courseId 课程 ID
 * @returns 完整的视频 URL，用于视频播放器
 */
export function getVideoUrl(courseId: string): string | undefined {
  const videoPath = getVideoPath(courseId);
  if (!videoPath) return undefined;
  
  // 直接返回public下的静态路径
  return `/${videoPath}`;
}
